    @Override
    public void actionPerformed(ActionEvent e) {

        if (e.getSource() == btnguardar) {
            if ((txtnombre.getText().length() == 0) || (txtdireccion.getText().length() == 0) || (txtsalario.getText().length() == 0)) {
                JOptionPane.showMessageDialog(null, "Debe completar los datos para continuar");

            } else {
                try {

                    Empleados empleado = new Empleados();
                    empleado.setNombre(txtnombre.getText().toString());
                    empleado.setDireccion(txtdireccion.getText().toString());
                    empleado.setTelefono(txttelefono.getText().toString());
                    empleado.setSalario(Double.parseDouble(txtsalario.getText().toString()));
                    empleado.setEstado(1);
                    empleados.create(empleado);
                    JOptionPane.showMessageDialog(null, "Datos registrados con Ã©xito");

                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "No es posible registrar los datos correctamente... por:" + ex.getMessage());

                }

            }

        }
        if (e.getSource() == btneditar) {
            try {

                Empleados empleado = new Empleados();
                empleado = empleados.findEmpleados(this.id_empledado);
                empleado.setNombre(txtnombre.getText().toString());
                empleado.setDireccion(txtdireccion.getText().toString());
                empleado.setTelefono(txttelefono.getText().toString());
                empleado.setSalario(Double.parseDouble(txtsalario.getText().toString()));
                empleado.setEstado(1);
                empleados.edit(empleado);
                JOptionPane.showMessageDialog(null, "Datos actualizados con Ã©xito");

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "No es posible editar los datos correctamente... por:" + ex.getMessage());

            }

        }
        if (e.getSource() == bnteliminar) {
            try {
                empleados.destroy(id_empledado);
                JOptionPane.showMessageDialog(null, "Datos eliminados con exito:");

            } catch (Exception ex) {

                JOptionPane.showMessageDialog(null, "No es posible eliminar los datos correctamente... por:" + ex.getMessage());
            }

        }

    }

    public void Consultar() {

        DefaultTableModel modelo = new DefaultTableModel();
        jtable.setModel(modelo);
        modelo.setRowCount(0);
        // valor count 0
        modelo.setColumnCount(0);
        modelo.addColumn("ID");
        modelo.addColumn("NOMBRE");
        modelo.addColumn("DIRECCION");
        modelo.addColumn("TELEFONO");
        modelo.addColumn("SALARIO");

        int objetos = empleados.findEmpleadosEntities().size();
        List<Empleados> lista = empleados.findEmpleadosEntities();

        for (Empleados r : lista) {
            modelo.addRow(new Object[]{r.getId(), r.getNombre(), r.getDireccion(), r.getTelefono(), r.getSalario()});
            System.out.println("" + r.getNombre());
        }
        /*establecemos el modelo  al Jtable llamado jTabla*/

    }

